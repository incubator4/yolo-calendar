const matrix = (cid: number) => {
  switch (cid) {
    case 1:
      return "#7EAAFE";
    case 2:
      return "#FDD475";
    case 3:
      return "#FDC0D0";
    case 4:
      return "#975540";
    case 5:
      return "#A50318";
    case 6:
      return "#39393B";
    default:
      return "#41b883";
  }
};

export default matrix;
